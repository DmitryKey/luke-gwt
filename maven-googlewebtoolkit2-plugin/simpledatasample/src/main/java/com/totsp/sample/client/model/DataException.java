package com.totsp.sample.client.model;



/**
 * Exception that can be thrown across the wire.
 * 
 * As of GWT 1.5 Exception implements Serializable and we don't need anything special here. 
 *
 * @author ccollins
 *
 */
public class DataException extends Exception {
    public DataException() {
    }

    public DataException(String message) {
        super(message);
    }
}
